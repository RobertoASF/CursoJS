// Incremento (igual que python)

let puntaje = 1;

console.log(puntaje);

puntaje += 3;
console.log(puntaje);

puntaje -= 3;
console.log(puntaje);


// ++ y -- siempre van de 1 en 1
puntaje ++;  // primero se muestra, luego se incrementa
console.log(puntaje);

++ puntaje; // primero se incrementa, luego se muestra
console.log(puntaje);

puntaje --  // primero se muestra, luego se decrementa
console.log(puntaje);

-- puntaje; // primero se decrementa, luego se muestra
console.log(puntaje);

